from __future__ import annotations

import json
from pathlib import Path

import pytest

pytest.importorskip("PIL")

from ai_fixtures.build_realistic_recompression_tiers import build_realistic_tiers
from ai_fixtures.copydays_loader import discover_copydays_images

from reclaim.ai.eval_harness import (
    DistributionDeclaration,
    EvalReport,
    assert_safe_to_promote_to_measured,
    current_commit_sha,
    precision_recall_curve,
    select_operating_point,
)
from reclaim.ai.phash import compute_image_hashes, hamming_distance

# Follow-up to evals/test_ai_copydays_gold.py: that eval's real PR curve was measured ONLY
# against Copydays' `strong` split (print-and-scan/blur/paint) — its single hardest,
# deliberately adversarial attack tier. GG flagged that measuring recall against the
# adversarial tail alone and treating it as THE operating distribution risks either
# under-crediting pHash (if real duplicates are mostly mild) or shipping a threshold nobody
# validated against the actual target case. This file measures the REALISTIC distribution
# instead: ordinary consumer duplicate accumulation (light re-save, moderate resize+recompress,
# format round-trip, messaging-app re-save) — generated programmatically from Copydays' 157
# real original photos (see build_realistic_recompression_tiers.py; Copydays' own milder
# `jpeg`/`crop` splits were not recoverable from any reachable mirror after a second search
# pass — see ADR-0012's follow-up section) — with the `hard` (strong-attack) tier kept and
# reported alongside for honest comparison, never discarded.
#
# Negatives are restricted to ORIGINAL-vs-ORIGINAL cross-block pairs only (not
# original-vs-strong-attacked cross-block pairs) — using a strong-attack-tainted image in a
# "negative" pair could make negatives look artificially easier to separate (heavy blur/paint
# pushes Hamming distance up for reasons unrelated to genuine dissimilarity), which would
# inflate the realistic-distribution precision estimate. Restricting to original-vs-original
# keeps the negative pool as clean, real, untainted "two different real photos" ground truth.
#
# Same posture as test_ai_copydays_gold.py: not in the default CI sweep (network + large
# download + several minutes of compute), skip cleanly when the dataset isn't present locally.
# Reproduce with:
#   uv run python evals/ai_fixtures/fetch_copydays.py
#   uv run pytest evals/test_ai_copydays_realistic_distribution.py -v -s

_COPYDAYS_ROOT = Path("data/ai_datasets/copydays/extracted")
_REALISTIC_ROOT = Path("data/ai_datasets/copydays/realistic_tiers")
_LOCKED_THRESHOLD = 14  # ADR-0012's currently-locked operating point, re-examined here
_MIN_RECALL_FLOOR = 0.5  # ADR-0016 policy default for this track: catch at least half of
# realistic duplicates, or the feature isn't worth shipping even at perfect precision.
_COMMAND = "uv run pytest evals/test_ai_copydays_realistic_distribution.py -v -s"
_FIXTURE = (
    "data/ai_datasets/copydays realistic tiers (ADR-0012 follow-up; generated by "
    "evals/ai_fixtures/build_realistic_recompression_tiers.py from real Copydays originals)"
)
_REPORT_PATH = Path("reports/ai/copydays_realistic_distribution_pr_curve.json")
_REALISTIC_DISTRIBUTION = DistributionDeclaration(
    description=(
        "5 deterministic transform profiles (mild recompress, mild resize, moderate "
        "resize+recompress, moderate PNG round-trip, messaging-app-style resave) applied to "
        "157 real Copydays original photos"
    ),
    is_realistic=True,
    is_adversarial_tail_only=False,
    is_synthetic_only=False,
    untested_variation_note=(
        "does not cover rotation, cropping, aspect-ratio changes, watermarks/overlays, "
        "color/contrast edits, heavier compression than quality=65, or combinations of these "
        "— ADR-0012's disclosed margin (threshold 14 vs. the bare-minimum 2 these 5 profiles "
        "need) is the mitigation, not a claim these profiles are exhaustive"
    ),
)

pytestmark = pytest.mark.skipif(
    not _COPYDAYS_ROOT.exists() or not any(_COPYDAYS_ROOT.glob("*.jpg")),
    reason=(
        "Copydays dataset not present locally — run "
        "`uv run python evals/ai_fixtures/fetch_copydays.py` first. Not auto-downloaded by "
        "this test or by CI (see module docstring)."
    ),
)


def test_recall_per_tier_and_realistic_pr_tradeoff() -> None:
    images = discover_copydays_images(_COPYDAYS_ROOT)
    originals = [img for img in images if img.is_original]
    assert len(originals) == 157

    variants = build_realistic_tiers([img.path for img in originals], _REALISTIC_ROOT)
    assert len(variants) == 785

    hash_cache: dict[Path, str] = {}

    def phash_of(path: Path) -> str:
        if path not in hash_cache:
            record = compute_image_hashes(path)
            assert record is not None, f"failed to decode {path}"
            hash_cache[path] = record.phash_hex
        return hash_cache[path]

    original_by_block = {img.block_id: img.path for img in originals}
    strong_by_block: dict[str, list[Path]] = {}
    for img in images:
        if not img.is_original:
            strong_by_block.setdefault(img.block_id, []).append(img.path)

    tier_pairs: dict[str, list[tuple[Path, Path]]] = {
        "hard": [],
        "mild": [],
        "moderate": [],
        "messaging_app": [],
    }
    for block_id, orig_path in original_by_block.items():
        for strong_path in strong_by_block.get(block_id, []):
            tier_pairs["hard"].append((orig_path, strong_path))
    for variant in variants:
        tier_pairs[variant.tier].append((variant.source_original, variant.path))

    assert len(tier_pairs["hard"]) == 229
    assert len(tier_pairs["mild"]) == 314
    assert len(tier_pairs["moderate"]) == 314
    assert len(tier_pairs["messaging_app"]) == 157

    # --- Per-tier recall at the currently-locked threshold (14) ---
    print(  # noqa: T201
        f"\n=== Per-tier recall at the locked threshold "
        f"(max_hamming_distance<={_LOCKED_THRESHOLD}) ==="
    )
    per_tier_recall: dict[str, float] = {}
    for tier in ("mild", "moderate", "messaging_app", "hard"):
        pairs = tier_pairs[tier]
        hits = sum(
            1 for a, b in pairs if hamming_distance(phash_of(a), phash_of(b)) <= _LOCKED_THRESHOLD
        )
        recall = hits / len(pairs)
        per_tier_recall[tier] = recall
        print(  # noqa: T201
            EvalReport(
                metric_name=f"copydays_recall_tier_{tier}_at_distance_{_LOCKED_THRESHOLD}",
                value=recall,
                commit_sha=current_commit_sha(),
                command=_COMMAND,
                fixture_path=_FIXTURE,
            )
        )
        print(f"    ({hits}/{len(pairs)} pairs caught)")  # noqa: T201

    # Sanity: pHash's designed sweet spot (resize/recompress) should trounce its designed
    # weak spot (print/scan/blur/paint) — if this ever inverts, something upstream is broken,
    # not just "recall happens to be low."
    assert per_tier_recall["mild"] > per_tier_recall["hard"], (
        "pHash recall on mild consumer-style duplicates should exceed recall on Copydays' "
        "adversarial `strong` tier — an inversion here means the hash pipeline itself needs "
        "investigation, not just the operating point"
    )

    # --- Realistic-distribution PR curve: mild+moderate+messaging_app positives only ---
    # (hard/strong deliberately excluded from THIS curve — it's reported per-tier above, not
    # blended into "the" operating distribution, which is the entire point of this eval.)
    negative_pairs: list[tuple[Path, Path]] = []
    original_paths = [img.path for img in originals]
    for i in range(len(original_paths)):
        for j in range(i + 1, len(original_paths)):
            negative_pairs.append((original_paths[i], original_paths[j]))
    assert len(negative_pairs) == 12_246  # C(157, 2) - clean original-vs-original negatives

    scored: list[tuple[float, bool]] = []
    for tier in ("mild", "moderate", "messaging_app"):
        for a, b in tier_pairs[tier]:
            distance = hamming_distance(phash_of(a), phash_of(b))
            scored.append((float(-distance), True))
    for a, b in negative_pairs:
        distance = hamming_distance(phash_of(a), phash_of(b))
        scored.append((float(-distance), False))

    curve = precision_recall_curve(scored, higher_score_is_more_similar=True)

    print(  # noqa: T201
        f"\n=== Realistic-distribution PR curve ({len(scored)} pairs, "
        f"{sum(1 for _, pos in scored if pos)} positive: mild+moderate+messaging_app only) ==="
    )
    # True cumulative precision/recall per cutoff (see evals/test_ai_copydays_gold.py's own
    # fix for why "last point per threshold," not first, is the correct cumulative value when
    # multiple pairs tie at the same raw Hamming distance).
    last_point_by_threshold: dict[float, object] = {}
    for point in curve:
        last_point_by_threshold[point.threshold] = point
    cumulative_table: dict[str, dict[str, float]] = {}
    for max_distance, point in sorted(
        ((int(-t), p) for t, p in last_point_by_threshold.items()), key=lambda pair: pair[0]
    ):
        if max_distance > 20:  # curve continues to precision-floor territory; not needed here
            break
        print(  # noqa: T201
            f"  max_hamming_distance<={max_distance:3d}  "
            f"precision={point.precision:.4f}  recall={point.recall:.4f}"
        )
        cumulative_table[str(max_distance)] = {
            "precision": point.precision,
            "recall": point.recall,
        }

    results_by_target: dict[str, dict[str, float]] = {}
    for target_precision in (0.95, 0.90, 0.85):
        operating_point = select_operating_point(
            curve,
            target_precision=target_precision,
            min_recall=_MIN_RECALL_FLOOR,
            distribution=_REALISTIC_DISTRIBUTION,
            source_description=(
                f"INRIA Copydays realistic distribution (ADR-0012 follow-up), "
                f"commit {current_commit_sha()}"
            ),
        )
        if operating_point is None:
            print(  # noqa: T201
                f"  target precision {target_precision}: NO point clears both the precision "
                f"target and the {_MIN_RECALL_FLOOR} recall floor"
            )
            results_by_target[str(target_precision)] = {
                "achievable": False,
                "max_hamming_distance": float("nan"),
                "precision": float("nan"),
                "recall": float("nan"),
            }
            continue
        max_distance = int(-operating_point.threshold)
        print(  # noqa: T201
            f"  target precision {target_precision}: max_hamming_distance={max_distance}  "
            f"precision={operating_point.precision:.4f}  recall={operating_point.recall:.4f}"
        )
        results_by_target[str(target_precision)] = {
            "achievable": True,
            "max_hamming_distance": float(max_distance),
            "precision": operating_point.precision,
            "recall": operating_point.recall,
        }

    # ADR-0016's structural check: THIS distribution (realistic, not adversarial/synthetic) is
    # the one ADR-0012 cites to justify MEASURED status — prove that's actually safe to do,
    # rather than trusting the ADR's prose alone.
    assert_safe_to_promote_to_measured(_REALISTIC_DISTRIBUTION)

    _REPORT_PATH.parent.mkdir(parents=True, exist_ok=True)
    _REPORT_PATH.write_text(
        json.dumps(
            {
                "commit_sha": current_commit_sha(),
                "command": _COMMAND,
                "fixture_path": _FIXTURE,
                "distribution": {
                    "description": _REALISTIC_DISTRIBUTION.description,
                    "is_realistic": _REALISTIC_DISTRIBUTION.is_realistic,
                    "is_adversarial_tail_only": _REALISTIC_DISTRIBUTION.is_adversarial_tail_only,
                    "is_synthetic_only": _REALISTIC_DISTRIBUTION.is_synthetic_only,
                    "untested_variation_note": _REALISTIC_DISTRIBUTION.untested_variation_note,
                },
                "min_recall_floor": _MIN_RECALL_FLOOR,
                "per_tier_recall_at_locked_threshold": {
                    "locked_threshold": _LOCKED_THRESHOLD,
                    **per_tier_recall,
                },
                "realistic_distribution_pr_tradeoff": results_by_target,
                "realistic_distribution_cumulative_curve": cumulative_table,
                "tier_pair_counts": {tier: len(pairs) for tier, pairs in tier_pairs.items()},
                "negative_pair_count": len(negative_pairs),
                "note": (
                    "Positives for the PR curve are mild+moderate+messaging_app only "
                    "(programmatically generated from real Copydays originals) — `hard` "
                    "(Copydays strong-attack split) is reported separately per-tier above, "
                    "not blended into the curve used for operating-point selection."
                ),
            },
            indent=2,
        )
    )
    print(f"\nFull report: {_REPORT_PATH}")  # noqa: T201

    # A floor, not a promotion of any specific value — see ADR-0012 for the actual chosen
    # operating point and its reasoning. This just confirms the realistic distribution is
    # meaningfully more tractable than the adversarial tier at SOME reasonable precision AND
    # recall (both floors, per ADR-0016 — not precision alone).
    assert results_by_target["0.85"]["achievable"], (
        "not even 0.85 precision (with the recall floor also met) is reachable on the "
        "realistic distribution — this would be a serious, reportable finding about pHash's "
        "viability for Feature 1a at all"
    )
